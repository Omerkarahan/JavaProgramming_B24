package day03_comments_escape_sequence;

public class EscapeSequence {

    public static void main(String[] args) {

        System.out.println("I love \"java\" programming"); // Here we use \" to put quatations around java

        System.out.println("I like to \'cook\'\\eat"); //this puts a single quote around cook

        System.out.println("Tomorrow is Friday.\nIt is a day off"); //
        System.out.println("\t\t\tMessage");



    }
}
